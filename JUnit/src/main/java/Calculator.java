public class Calculator {

    public float additionFloat(float x, float y) {
        return x + y;
    }

    public double additionDouble(double x, double y) {
        return x + y;
    }

    public float subtractionFloat(float x, float y) {
        return x - y;
    }

    public double subtractionDouble(double x, double y) {
        return x - y;
    }

    public float multiplicationFloat(float x, float y) {
        return x * y;
    }

    public double multiplicationDouble(double x, double y) {
        return x * y;
    }

    public float divisionFloat(float x, float y) {
        return x / y;
    }

    public double divisionDouble(double x, double y) {
        return x / y;
    }
}
